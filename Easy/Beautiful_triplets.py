def beautifulTriplets(d, arr):
    # Write your code here
    dic={}
    count=0
    for i in range(len(arr)):
        if arr[i] not in dic.keys():
            dic[arr[i]]=1
    # print("the dict is: ", dic)
    for i in range(len(arr)):
        if (arr[i]+d in dic.keys()) and (arr[i]+2*d in dic.keys()):
            count+=1
    return count

